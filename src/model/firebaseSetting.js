var mongoose = require('mongoose');
var Schema = mongoose.Schema;

var FirebaseSettingSchema = new Schema({
  

  private_key: {
    type: String,
    required: true,
    select: false
  },
  client_email: {
    type: String,
    required: true,
    select: false
  },
  firebase_project_id: {
    type: String,
    required: true,
    select: false
  }
  
},{
  timestamps: true
}
);

module.exports = mongoose.model('firebaseSetting', FirebaseSettingSchema);
